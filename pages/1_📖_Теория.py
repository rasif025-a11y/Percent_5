import streamlit as st

st.title("📖 Теоретический материал")

st.markdown("""
Изучите основные сведения по теме **«Проценты»**.
Раскрывайте интересующий раздел и знакомьтесь с примерами решения задач.
""")

# -----------------------------------------------------

with st.expander("📌 Что такое процент?"):

    st.write("""
**Процент** — это одна сотая часть числа.

Процент обозначается знаком **%** и показывает,
какую часть составляет число от целого.

Например:

- 1% = 0,01
- 10% = 0,1
- 100% = целое число
""")

# -----------------------------------------------------

with st.expander("🔄 Перевод процентов в дробь"):

    st.table({
        "Процент": [
            "1%",
            "5%",
            "10%",
            "20%",
            "25%",
            "50%",
            "75%",
            "100%"
        ],
        "Десятичная дробь": [
            "0.01",
            "0.05",
            "0.1",
            "0.2",
            "0.25",
            "0.5",
            "0.75",
            "1"
        ]
    })

# -----------------------------------------------------

with st.expander("🧮 Нахождение процента от числа"):

    st.write("""
Чтобы найти процент от числа, можно выполнить два последовательных действия:

1. Разделить число на **100** (найти 1%).
2. Полученный результат умножить на нужное количество процентов.
""")

    st.code("""
Найти 25% от 400

1) 400 ÷ 100 = 4
2) 4 × 25 = 100

Ответ: 100
""")

# -----------------------------------------------------

with st.expander("🧮 Нахождение числа по проценту"):

    st.write("""
Если известна часть числа и процент,
используется формула:

**Часть × 100 ÷ Процент**
""")

    st.code("""
20 составляет 10%.

20 × 100 ÷ 10 = 200
""")

# -----------------------------------------------------

with st.expander("📊 Нахождение процентного отношения"):

    st.write("""
Чтобы узнать, сколько процентов составляет одно число от другого:

**Часть ÷ Целое × 100**
""")

    st.code("""
15 от 60

15 ÷ 60 × 100 = 25%
""")

# -----------------------------------------------------

with st.expander("📈 Увеличение числа на процент"):

    st.write("""
Для увеличения числа на процент применяется формула:

**Число × (1 + p/100)**
""")

    st.code("""
500 увеличить на 20%

500 × 1.2 = 600
""")

# -----------------------------------------------------

with st.expander("📉 Уменьшение числа на процент"):

    st.write("""
Для уменьшения числа на процент применяется формула:

**Число × (1 - p/100)**
""")

    st.code("""
500 уменьшить на 20%

500 × 0.8 = 400
""")

# -----------------------------------------------------

with st.expander("🌍 Использование процентов в жизни"):

    st.write("""
Проценты широко используются:

• при расчёте скидок в магазинах;

• при начислении банковских процентов;

• при анализе статистических данных;

• при определении успеваемости учащихся;

• в экономике и финансовых расчётах;

• в медицине и научных исследованиях.
""")

# -----------------------------------------------------

