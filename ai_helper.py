def explain(question):

    question = question.lower()

    # Найти процент от числа
    if "найдите" in question and "%" in question and "от числа" in question:
        return """
🤖 Объяснение

1. Переведите процент в десятичную дробь.
Например, 20% = 0.2.

2. Умножьте число на эту дробь.

Пример:
300 × 0.2 = 60.

Полученное число и есть ответ.
"""

    # Найти число по проценту
    elif "какого числа" in question:
        return """
🤖 Объяснение

1. Переведите процент в десятичную дробь.

2. Разделите известную часть на эту дробь.

Например:

60 составляет 20% числа.

20% = 0.2

60 ÷ 0.2 = 300.

Ответ: 300.
"""

    # Процентное отношение
    elif "сколько процентов составляет" in question:
        return """
🤖 Объяснение

1. Разделите первое число на второе.

2. Умножьте результат на 100%.

Например:

15 ÷ 60 = 0.25

0.25 × 100 = 25%.

Ответ: 25%.
"""

    # Увеличение числа
    elif "увеличьте" in question:
        return """
🤖 Объяснение

1. Найдите указанный процент от числа.

2. Прибавьте его к исходному числу.

Например:

250 + 20%

20% от 250 = 50

250 + 50 = 300.
"""

    # Уменьшение числа
    elif "уменьшите" in question:
        return """
🤖 Объяснение

1. Найдите указанный процент от числа.

2. Вычтите его из исходного числа.

Например:

300 − 10%

10% от 300 = 30

300 − 30 = 270.
"""

    else:
        return """
🤖 Не удалось определить тип задачи.

Попробуйте решить её по общему правилу:
- определите, что составляет 100%;
- переведите процент в дробь;
- выполните вычисления.
"""